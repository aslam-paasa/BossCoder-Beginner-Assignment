package _11_Maths;

public class _01_Count {
    public static void main(String[] args) {

        /**
         * Count 1 to 5
         */

        int i = 1;

        while (i <= 5) {
            System.out.print(i + " "); // horizontal
            i++;
        }
        System.out.println("done");
    }

}
